function openNav() {
    var overlay = document.getElementById('myNav');
    var openit = document.getElementById('openit');
    var closeit = document.getElementById('closeit');
    overlay.style.display = 'block';
    closeit.style.display = 'block';
    openit.style.display = 'none';
  }
  
  function closeNav() {
    var overlay2 = document.getElementById('myNav');
    var openit2 = document.getElementById('openit');
    var closeit2 = document.getElementById('closeit');
    overlay2.style.display = 'none';
    closeit2.style.display = 'none';
    openit2.style.display = 'block';
  }
  